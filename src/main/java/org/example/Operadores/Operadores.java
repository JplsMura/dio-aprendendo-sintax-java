package org.example.Operadores;

public class Operadores {
    public static void main(String[] args) {
        int numero = 5;
        // torna o numero negativo
        numero = - numero;
        System.out.println(numero);

        // outra forma de tornar o numero positivo
        numero = numero * -1;
        System.out.println(numero);
    }
}
