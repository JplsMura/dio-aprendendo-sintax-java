package org.example.Operadores;

public class OperadoresBoolean {
    public static void main(String[] args) {
        boolean verdadeiro = true;
        boolean falso = false;

        // nega o valor da variavel verdadeiro
        System.out.println(!verdadeiro);

        // nega o valor da variavel falso
        System.out.println(!falso);

        // compara se o valor da variavel verdadeiro é igual ao valor da variavel falso
        System.out.println(verdadeiro == falso);

        // compara se o valor da variavel verdadeiro é diferente do valor da variavel falso
        System.out.println(verdadeiro != falso);
    }
}
