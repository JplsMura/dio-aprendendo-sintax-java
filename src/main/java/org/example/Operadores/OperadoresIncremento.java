package org.example.Operadores;

public class OperadoresIncremento {
    public static void main(String[] args) {
        int numero = 5;
        // incrementa o numero em 1
        numero = numero + 1;
        System.out.println(numero);

        // outra forma de incrementar o numero em 1
        numero++;
        System.out.println(numero);

        // decrementa o numero em 1
        numero = numero - 1;
        System.out.println(numero);

        // outra forma de decrementar o numero em 1
        numero--;
        System.out.println(numero);
    }
}
